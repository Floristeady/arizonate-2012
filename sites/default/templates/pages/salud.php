    <h3>BENEFICIOS DEL TÉ VERDE</h3>
      <p>Posiblemente usted ya sepa que AriZona usa auténtico Té Verde como primer ingrediente. AriZona brinda la experiencia del "Té Total", conservando los beneficios para la salud inherentes al té.</p>
        <p> Después del agua, el té es la bebida más consumida en el mundo. La gente
          lo ha estado bebiendo por miles de años, por lo cual hemos tenido un buen tiempo para aprender sobre él.  </p>
    <p><strong>¿La Razón?</strong></p>
        <p> Usted puede beber té por su sabor y por sus propiedades relajantes, pero hay evidencias muy convincentes de que beber té también puede traer importantes beneficios para su salud. Basta con beberlo a diario (1 o 2 tazas es suficiente) y los resultados, a largo plazo, serán extraordinarios. Interiorízate en el tema.</p>
