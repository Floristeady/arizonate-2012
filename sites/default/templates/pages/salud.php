	<h3>TÉ BLANCO</h3>
      <p>Su principal propiedad es su alta capacidad antioxidante. Contiene tres veces más polifenoles (un potente antioxidante presente en todos los tipos de té) que las otras variedades de té, ya que está sustancia se va perdiendo a medida que el té se va procesando. En vez de utilizar la hoja entera, como
se hace con el Té verde, se recogen solo los brotes que es donde se concentra la mayor energía de la planta. Estos brotes, aun sin abrir, están cubiertos con vellos blancos (de ahí su nombre).</p>
        <p>Los investigadores aseguran que este proceso tan simple es lo que mantiene al té en un estado puro, lleno de energía y tres veces más antioxidante que el té verde. Tiene la mitad de cafeína que el Té verde.</p>
